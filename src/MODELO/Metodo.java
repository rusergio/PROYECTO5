public class Metodo {
	private String genero = "";
	private String confirma = "";
	private String tieneLicenc = "";
	private String date;
	
	public Metodo() {
		
	}
	
	public String getSexo() {
		return genero;
	}

	public void setSexo(String genero) {
		this.genero = genero;
	}

	public String getConfirma() {
		return confirma;
	}

	public void setConfirma(String confirma) {
		this.confirma = confirma;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getTieneLicenc() {
		return tieneLicenc;
	}

	public void setTieneLicenc(String tieneLicenc) {
		this.tieneLicenc = tieneLicenc;
	}

	

	
	
	
	
}
